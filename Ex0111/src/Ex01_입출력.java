
public class Ex01_입출력 {

	public static void main(String[] args) {
		// 출력 명령어 작성하기!
		// 잠깐! 컴퓨터는 숫자 VS 문자
		System.out.println(54); // 숫자
		System.out.println("54"); // 문자
		// ctrl + s 저장 단축키!
		// 팀원들 이름 다섯명 출력
		System.out.println(10 + 10); // 10 +10의 결과를 출력해라!
		System.out.println(5 - 9); // 5-9의 결과를 출력해라!
		// ctrl + shift + f => 정렬 단축키
		System.out.println(5.0 / 2.0); // 정수 나누기 실수는 실수,정수/정수=정수
		System.out.println(5f/2);      //실수를 표기하는 방법 float,double //2.0 ->double
        //출력, 연산, 형변환
		System.out.println(5+"1"+5);
		System.out.println(5+1+"5");
		System.out.println(5+(1+"5"));
		System.out.println("5"+1+5);
		
		
		
		
	}

}
